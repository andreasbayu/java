    private void loadTable() {
        try {
            model.setRowCount(0);
            Connection conn = Koneksi.getKoneksi();
            Statement stat  = conn.createStatement();
            String sql = "SELECT b.id as b_id, b.nama as b_nama, b.stok as b_stok, "
                    + "k.nama as k_nama, "
                    + "a.nama as a_nama "
                    + "FROM barang b "
                    + "JOIN kategori_barang k ON b.kategori_id = k.id "
                    + "JOIN admin a ON b.admin_id = a.id";
            ResultSet rs    = stat.executeQuery(sql);
            
            while (rs.next()) {
                Object[] objek = new Object[5];
                objek[0] = rs.getInt("b_id");
                objek[1] = rs.getString("b_nama");
                objek[2] = rs.getInt("b_stok");
                objek[3] = rs.getString("k_nama");
                objek[4] = rs.getString("a_nama");
                model.addRow(objek);
            }
            
            if (cmbKategori.getItemCount() < 2) setComboKategori();
            
        } catch (SQLException e) {
            System.out.println(e);
        }
    }
    
