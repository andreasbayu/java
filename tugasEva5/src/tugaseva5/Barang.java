    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try {
            
            if (cmbKategori.getSelectedIndex() == 0) { 
                JOptionPane.showMessageDialog(this, "Kategori masih kosong");
            } else {            
                Connection conn         = Koneksi.getKoneksi();
                String sql              = "INSERT INTO barang (nama, stok, kategori_id, admin_id) VALUES (?, ?, ?, ?)";
                PreparedStatement ps    = conn.prepareStatement(sql);

                String getSelectedItem = (String) cmbKategori.getSelectedItem();

                ps.setString(1, txtNama.getText());
                ps.setInt(2, Integer.parseInt(txtStock.getText()));
                ps.setString(3, kategoriBarang.get(getSelectedItem));
                ps.setInt(4, adminId);
                ps.execute();

                clearText();
                loadTable();
                JOptionPane.showMessageDialog(this, "Berhasil menyimpan data");
            }
            
        } catch (SQLException e) {
            System.out.println("Terjadi masalah : " + e);
        }
        
    }//GEN-LAST:event_btnSimpanActionPerformed

