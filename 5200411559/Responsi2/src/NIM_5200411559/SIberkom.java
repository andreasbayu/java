    private void tblSiberkomMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSiberkomMouseClicked
        // TODO add your handling code here:
        int getSelectedRow   = tblSiberkom.getSelectedRow();
        String id            =  getFromTable(getSelectedRow, 0).toString();
        String namab         = getFromTable(getSelectedRow, 1).toString();
        String unit          = getFromTable(getSelectedRow, 2).toString();
        String jenis         = getFromTable(getSelectedRow, 3).toString();
        String penerima      = getFromTable(getSelectedRow, 4).toString();
        String supplier      = getFromTable(getSelectedRow, 5).toString();
        String hargaPerunit  = getFromTable(getSelectedRow, 6).toString();
        String catatan       = getFromTable(getSelectedRow, 7).toString();
        String tanggal       = getFromTable(getSelectedRow, 8).toString();
        
        // split tanggal
        String[] splitTanggal = tanggal.split("-");
        
        txtId.setText(id);
        txtNamab.setText(namab);
        txtJumlahu.setText(unit);
        if (jenis.equals("lokal")) rdLokal.setSelected(true); else rdImpor.setSelected(true);
        cmbPenerima.setSelectedItem(penerima);
        cmbSupplier.setSelectedItem(supplier);
        txtHargap.setText(hargaPerunit);
        txaCatatan.setText(catatan);
        txtTanggal.setText(splitTanggal[2]);
        txtBulan.setText(splitTanggal[1]);
        txtTahun.setText(splitTanggal[0]);

        btnSimpan.setEnabled(false);
        btnEdit.setEnabled(true);
        btnHapus.setEnabled(true);

    }//GEN-LAST:event_tblSiberkomMouseClicked

    private Object getFromTable(int getSelectedRow, int numberOfColumn) {
        return tblSiberkom.getModel().getValueAt(getSelectedRow, numberOfColumn);
    }
    
