    private void loadTable() {
        model.setRowCount(0);
        try {
            Connection conn = Koneksi.getKoneksi();
            String sql = "SELECT * FROM siberkom";
            Statement st = conn.createStatement();
            ResultSet res = st.executeQuery(sql);
            
            while (res.next()) {                
                Object[] object = new Object[9];
                object[0] = res.getInt("id");
                object[1] = res.getString("nama_barang");
                object[2] = res.getInt("jumlah_unit");
                object[3] = res.getString("jenis_produk");
                object[4] = res.getString("penerima");
                object[5] = res.getString("supplier");
                object[6] = res.getInt("harga_perunit");
                object[7] = res.getString("catatan");
                object[8] = res.getString("tanggal");
                model.addRow(object);
            }
            
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Terjadi Masalah : "+e);
        }
    }
    
    
    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try {

            Connection conn         = Koneksi.getKoneksi();
            String sql              = "INSERT INTO siberkom (nama_barang, jumlah_unit, jenis_produk, penerima, supplier, harga_perunit, catatan, tanggal)"+
                                      " VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement ps    = conn.prepareStatement(sql);

            String selectedRadio    = "";

            if (rdLokal.isSelected()) {
                selectedRadio = "lokal";
            } else if (rdImpor.isSelected()) {
                selectedRadio = "impor";
            }             

            ps.setString(1, txtNamab.getText());
            ps.setInt(2, Integer.parseInt(txtJumlahu.getText()));
            ps.setString(3, selectedRadio);
            ps.setString(4, cmbPenerima.getSelectedItem().toString());
            ps.setString(5, cmbSupplier.getSelectedItem().toString());
            ps.setInt(6, Integer.parseInt(txtHargap.getText()));
            ps.setString(7, txaCatatan.getText());
            ps.setString(8, txtTahun.getText()+"-"+txtBulan.getText()+"-"+txtTanggal.getText());

            int insertedRow = ps.executeUpdate();

            loadTable();
            if (insertedRow > 0) {
                JOptionPane.showMessageDialog(this, "Berhasil menyimpan data");
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menyimpan data");
            }
            bersihkan();

        } catch (SQLException e) {
            System.out.println("Terjadi masalah : " + e);
        }

    }//GEN-LAST:event_btnSimpanActionPerformed

